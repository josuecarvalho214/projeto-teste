private void listarVendas() {
    try {
        VendaDAO dao = new VendaDAO();
        List<Vendas> lista = dao.listar(); 
        DefaultTableModel modelo = (DefaultTableModel) tblVendas.getModel();
        modelo.setRowCount(0); 
        for (Vendas v : lista) {
            modelo.addRow(new Object[]{v.getCodigo(), v.getNomeCliente(), v.getCpf(), v.getTotal()});
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao listar vendas: " + e.getMessage());
    }
}


private void listarProdutos() {
    try {
        ProdutoDAO dao = new ProdutoDAO();
        List<Produtos> lista = dao.listar();
        DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
        modelo.setRowCount(0); 
        for (Produtos p : lista) {
            modelo.addRow(new Object[]{p.getCodigo(), p.getDescricao(), p.getValor()});
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao listar produtos: " + e.getMessage());
    }
}
    
private double calcularTotalItens() {
        double total = 0.0;
        DefaultTableModel modelo = (DefaultTableModel) tblItensVenda.getModel();
        for (int i = 0; i < modelo.getRowCount(); i++) {
            total += (double) modelo.getValueAt(i, 2); 
        }
        return total;
    }

    

