    private void btnAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarProdutoActionPerformed
        // TODO add your handling code here:
     int linha = tblProdutos.getSelectedRow();
        if (linha >= 0) {
            int codigo = (int) tblProdutos.getValueAt(linha, 0);
            String descricao = (String) tblProdutos.getValueAt(linha, 1);
            double valor = (double) tblProdutos.getValueAt(linha, 2);

            DefaultTableModel modelo = (DefaultTableModel) tblItensVenda.getModel();
            modelo.addRow(new Object[]{codigo, descricao, valor});

            
            txtTotal.setText(String.valueOf(calcularTotalItens()));
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para adicionar.");
        }
    }//GEN-LAST:event_btnAdicionarProdutoActionPerformed

    private void btnRemoverProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverProdutoActionPerformed
        // TODO add your handling code here:
        
    
        int linha = tblItensVenda.getSelectedRow();
        if (linha >= 0) {
            DefaultTableModel modelo = (DefaultTableModel) tblItensVenda.getModel();
            modelo.removeRow(linha);

            
            txtTotal.setText(String.valueOf(calcularTotalItens()));
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para remover.");
        }

        
    }//GEN-LAST:event_btnRemoverProdutoActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
  try {
        String nomeCliente = txtNomeCliente.getText();
        String cpf = txtCPF.getText();

        if (nomeCliente.isEmpty() || cpf.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o nome e CPF do cliente.");
            return;
        }

        Vendas venda = new Vendas();
        venda.setNomeCliente(nomeCliente);
        venda.setCpf(cpf);

       
        DefaultTableModel modelo = (DefaultTableModel) tblItensVenda.getModel();
        for (int i = 0; i < modelo.getRowCount(); i++) {
            Produtos p = new Produtos();
            p.setCodigo((int) modelo.getValueAt(i, 0));
            p.setDescricao((String) modelo.getValueAt(i, 1));
            p.setValor((double) modelo.getValueAt(i, 2));
            venda.getItens().add(p);
        }

        if (venda.getItens().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Adicione pelo menos um produto à venda.");
            return;
        }

       
        double total = 0;
        for (Produtos p : venda.getItens()) {
            total += p.getValor();
        }
        venda.setTotal(total);

       
        VendaDAO dao = new VendaDAO();
        dao.cadastrar(venda);

        
        listarVendas();

       
        txtNomeCliente.setText("");
        txtCPF.setText("");
        txtTotal.setText(""); 
        modelo.setRowCount(0);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao cadastrar venda: " + e.getMessage());
    }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        int linha = tblVendas.getSelectedRow(); 
    if (linha >= 0) {
        Object obj = tblVendas.getValueAt(linha, 0); 
        if (obj != null) {
            try {
                int codigoVenda = Integer.parseInt(obj.toString()); 
                VendaDAO dao = new VendaDAO();
                dao.excluir(codigoVenda); 
                listarVendas(); 
                JOptionPane.showMessageDialog(null, "Venda excluída com sucesso!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao excluir venda: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Código da venda inválido!");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma venda para excluir.");
    }
    
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        
                                                    
    int linha = tblVendas.getSelectedRow();
    if (linha >= 0) {
        Object obj = tblVendas.getValueAt(linha, 0); 
        if (obj != null) {
            try {
                int codigoVenda = Integer.parseInt(obj.toString()); 
                String nomeCliente = txtNomeCliente.getText();
                String cpf = txtCPF.getText();

                Vendas venda = new Vendas();
                venda.setCodigo(codigoVenda); 
                venda.setNomeCliente(nomeCliente);
                venda.setCpf(cpf);
                
                DefaultTableModel modeloItens = (DefaultTableModel) tblItensVenda.getModel();
                venda.getItens().clear(); 
                for (int i = 0; i < modeloItens.getRowCount(); i++) {
                    Produtos p = new Produtos();
                    p.setCodigo((int) modeloItens.getValueAt(i, 0));
                    p.setDescricao((String) modeloItens.getValueAt(i, 1));
                    p.setValor((double) modeloItens.getValueAt(i, 2));
                    venda.getItens().add(p);
                }

                
                double total = 0;
                for (Produtos p : venda.getItens()) {
                    total += p.getValor();
                }
                venda.setTotal(total);
                
                
                VendaDAO dao = new VendaDAO();
                dao.alterar(venda);

                listarVendas(); 
                JOptionPane.showMessageDialog(null, "Venda alterada com sucesso!\nTotal: R$ " + total);

               
                txtNomeCliente.setText("");
                txtCPF.setText("");
                DefaultTableModel modeloLimpar = (DefaultTableModel) tblItensVenda.getModel();
                modeloLimpar.setRowCount(0);
                
                } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao alterar venda: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Código da venda inválido!");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma venda para alterar.");
    }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

